    private void senderTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_senderTableMouseClicked
        // TODO add your handling code here:
        int selectedRow = senderTable.getSelectedRow();
        if (selectedRow != -1) {
            message.setText(" row selected:" + Integer.parseInt(senderTable.getValueAt(selectedRow, 0).toString()));
            emailAddress.setText(senderTable.getValueAt(selectedRow, 1).toString());
        }
    }//GEN-LAST:event_senderTableMouseClicked

    private void upateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_upateBtnMouseClicked
        // TODO add your handling code here:
        int selectedRow = senderTable.getSelectedRow();
        if (selectedRow != -1) {
            if (JOptionPane.showConfirmDialog(this, "Do you really want to update ") == 0) {
                try {
                    Sender sender = senderJpaController.findSender(Integer.parseInt(senderTable.getValueAt(selectedRow, 0).toString()));
                    sender.setEmailAddress(emailAddress.getText());
                    senderJpaController.edit(sender);
                } catch (Exception ex) {
                    Logger.getLogger(SenderView.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        }
        emailAddress.setText(null);
        message.setText(" row updated:" + Integer.parseInt(senderTable.getValueAt(selectedRow, 0).toString()));
        loadData();

    }//GEN-LAST:event_upateBtnMouseClicked

    private void deleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBtnMouseClicked
        // TODO add your handling code here:
        int[] selectedRows = senderTable.getSelectedRows();
        if (selectedRows.length > 0) {
            if (JOptionPane.showConfirmDialog(this, "Do you really want to delete rows:" + Arrays.toString(selectedRows)) == 0) {
                for (int selectedRow : selectedRows) {
                    Integer id = Integer.parseInt(senderTable.getValueAt(selectedRow, 0).toString());
                    try {
                        try {
                            senderJpaController.destroy(id);
                        } catch (IllegalOrphanException ex) {
                            Logger.getLogger(SenderView.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } catch (NonexistentEntityException ex) {
                        Logger.getLogger(RecipientForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    
                }
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Please select at least one row:", "Alert", JOptionPane.WARNING_MESSAGE);
        }
        message.setText("data deleted");
        loadData();
    }//GEN-LAST:event_deleteBtnMouseClicked


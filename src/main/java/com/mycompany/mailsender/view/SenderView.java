 private void addSender() {
        Sender sender = new Sender();
        sender.setEmailAddress(emailAddress.getText());
        
        senderJpaController.create(sender);
        emailAddress.setText(null);
        message.setText("data inserted");
        loadData();
        
    }
    
    private List<Sender> findAllSender() {
        return senderJpaController.findSenderEntities();
    }
    
    private void loadData() {
        String[] column = {"id", "email", "description"};
        Object[][] data = (Object[][]) findAllSender().stream()
                .map(sender -> new Object[]{sender.getId(), sender.getEmailAddress(), sender.getDescription(),})
                .toArray(Object[][]::new);
        
        senderTable.setModel(new javax.swing.table.DefaultTableModel(data, column));
        
    }

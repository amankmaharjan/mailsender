    private void sendBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_sendBtnMouseClicked
        // TODO add your handling code here:

        Email email = new Email();
        email.setSubject(subjectField.getText());
        email.setMessage(messageField.getText());
//        email.setDuration(Integer.parseInt(duration.getText()));

        List<String> receipientNameList = Arrays.asList(recipeintListTextField.getText().split(","));

        String sender = senderDropdown.getSelectedItem().toString();
        String password = String.copyValueOf(jPasswordField1.getPassword());
        try {
           this.dispose();
            MailSenderUtil.configureMail(email, sender, password, receipientNameList);
            JOptionPane pane = new JOptionPane("Mail sent",
                    JOptionPane.INFORMATION_MESSAGE);

            JDialog dialog = pane.createDialog(null, "Title");
            dialog.setModal(false);
            dialog.setVisible(true);
            new MainFrame().setVisible(true);
            this.loadAll();
//            this.setVisible(true);

        } catch (Exception ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_sendBtnMouseClicked


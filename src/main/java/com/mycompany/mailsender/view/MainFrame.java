    private void loadAll() {
        List<Sender> senderList = senderJpaController.findSenderEntities();
        List<Recipient> recipientList = recipientJpaController.findRecipientEntities();
        String recipientListValue = recipientList.stream().map(l -> l.getEmailAddress().toString()).collect(Collectors.joining(","));
        recipeintListTextField.setText(recipientListValue);
        senderDropdown.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{}));
        senderList.forEach(l -> senderDropdown.addItem(l.getEmailAddress()));

    }
